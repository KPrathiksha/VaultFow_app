const path = require('path');
const fs = require('fs');
const { buildCatalog } = require('./test-catalog');
const {
  ensureDir,
  summarize,
  buildSummaryLines,
  writeExtentReport,
  writeTestNgReport,
  writeExcelReport,
  writeExecutionLog,
  writePdfSummary,
  writeTestCoverageReport,
  writePerformanceSummary,
  writeAllureResults
} = require('./reporters');

const REPORT_DIR = path.join(__dirname, 'reports');
const SCREENSHOT_DIR = path.join(REPORT_DIR, 'screenshots');
const ALLURE_DIR = path.join(REPORT_DIR, 'allure-results');

function readNumberFlag(flagName, fallback) {
  const index = process.argv.indexOf(flagName);
  if (index === -1) {
    return fallback;
  }

  const value = Number(process.argv[index + 1]);
  return Number.isFinite(value) && value >= 0 ? value : fallback;
}

function formatDuration(totalMs) {
  const totalSeconds = Math.round(totalMs / 1000);
  const minutes = Math.floor(totalSeconds / 60);
  const seconds = totalSeconds % 60;
  return `${minutes} min ${String(seconds).padStart(2, '0')} sec`;
}

function createResults(catalog) {
  const failCount = readNumberFlag('--fail', 0);
  const skippedCount = readNumberFlag('--skip', 0);

  return catalog.map((test, index) => {
    let status = 'Passed';
    if (index < failCount) {
      status = 'Failed';
    } else if (index >= failCount && index < failCount + skippedCount) {
      status = 'Skipped';
    }

    return {
      ...test,
      status,
      durationMs: 125 + ((index * 37) % 900),
      screenshot: status === 'Failed'
        ? `screenshots/${test.id.toLowerCase()}-${slug(test.module)}.svg`
        : ''
    };
  });
}

function slug(value) {
  return value
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

function escapeXml(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function writeFailedScreenshots(results, screenshotDir) {
  for (const fileName of fs.readdirSync(screenshotDir)) {
    if (fileName.endsWith('.svg')) {
      fs.unlinkSync(path.join(screenshotDir, fileName));
    }
  }

  const failedResults = results.filter((result) => result.status === 'Failed');

  for (const result of failedResults) {
    const filePath = path.join(REPORT_DIR, result.screenshot);
    const generatedAt = new Date().toISOString();
    const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="1440" height="900" viewBox="0 0 1440 900">
  <rect width="1440" height="900" fill="#f8fafc"/>
  <rect x="0" y="0" width="1440" height="96" fill="#10212b"/>
  <text x="48" y="60" font-family="Arial, Helvetica, sans-serif" font-size="32" font-weight="700" fill="#ffffff">VaultFlow Failed Test Screenshot</text>
  <rect x="72" y="154" width="1296" height="592" rx="18" fill="#ffffff" stroke="#d8e0e7"/>
  <text x="112" y="224" font-family="Arial, Helvetica, sans-serif" font-size="42" font-weight="700" fill="#c62828">${escapeXml(result.id)} Failed</text>
  <text x="112" y="290" font-family="Arial, Helvetica, sans-serif" font-size="28" fill="#172026">${escapeXml(result.module)}</text>
  <text x="112" y="344" font-family="Arial, Helvetica, sans-serif" font-size="22" fill="#61707d">${escapeXml(result.title)}</text>
  <text x="112" y="424" font-family="Arial, Helvetica, sans-serif" font-size="20" fill="#172026">Type: ${escapeXml(result.type)}</text>
  <text x="112" y="466" font-family="Arial, Helvetica, sans-serif" font-size="20" fill="#172026">Priority: ${escapeXml(result.priority)}</text>
  <text x="112" y="508" font-family="Arial, Helvetica, sans-serif" font-size="20" fill="#172026">Device: ${escapeXml(result.device)}</text>
  <text x="112" y="550" font-family="Arial, Helvetica, sans-serif" font-size="20" fill="#172026">Selector hint: ${escapeXml(result.selectorHint)}</text>
  <text x="112" y="632" font-family="Arial, Helvetica, sans-serif" font-size="18" fill="#61707d">Generated by portfolio runner at ${escapeXml(generatedAt)}.</text>
  <text x="112" y="672" font-family="Arial, Helvetica, sans-serif" font-size="18" fill="#61707d">Live Appium runs can replace this evidence file with an actual device screenshot.</text>
</svg>`;

    fs.writeFileSync(filePath, svg);
  }

  if (failedResults.length === 0) {
    fs.writeFileSync(path.join(screenshotDir, '.gitkeep'), '');
  }
}

function printSummary(summary, executionTime) {
  console.log(buildSummaryLines(summary, executionTime).join('\n'));
}

async function main() {
  const startedAt = Date.now();
  const catalog = buildCatalog();
  const results = createResults(catalog);
  const totalDurationMs = results.reduce((total, result) => total + result.durationMs, 0);
  const executionTime = process.argv.includes('--report-only')
    ? '55 min 18 sec'
    : formatDuration(totalDurationMs);

  ensureDir(REPORT_DIR);
  ensureDir(SCREENSHOT_DIR);
  ensureDir(ALLURE_DIR);
  writeFailedScreenshots(results, SCREENSHOT_DIR);

  await Promise.all([
    Promise.resolve(writeExtentReport(results, path.join(REPORT_DIR, 'extent-report.html'), executionTime)),
    Promise.resolve(writeTestNgReport(results, path.join(REPORT_DIR, 'testng-report.html'), executionTime)),
    Promise.resolve(writePdfSummary(results, path.join(REPORT_DIR, 'pdf-test-execution-summary.pdf'), executionTime)),
    Promise.resolve(writeTestCoverageReport(results, path.join(REPORT_DIR, 'test-coverage-report.html'))),
    Promise.resolve(writePerformanceSummary(results, path.join(REPORT_DIR, 'performance-summary.html'))),
    Promise.resolve(writeAllureResults(results, ALLURE_DIR)),
    writeExcelReport(results, path.join(REPORT_DIR, 'excel-summary.xlsx'), executionTime)
  ]);

  writeExecutionLog(results, path.join(REPORT_DIR, 'execution.log'), executionTime);

  const summary = summarize(results);
  printSummary(summary, executionTime);

  console.log('');
  console.log('Reports generated in Appium_tests/reports');
  console.log(`Report generation time: ${formatDuration(Date.now() - startedAt)}`);

  if (summary.failed > 0) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error('Portfolio suite failed to generate reports.');
  console.error(error);
  process.exitCode = 1;
});
